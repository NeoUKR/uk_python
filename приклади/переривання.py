from пітон import Переривання, надрукувати, текст


class МоєПереривання(Переривання):
    повідомлення: текст

    def __init__(я, повідомлення: текст, *арг, **карг):
        super().__init__(повідомлення, *арг, **карг)
        я.повідомлення = повідомлення


try:
    raise МоєПереривання("Навмисне переривання програми")
except МоєПереривання as п:
    надрукувати("Виникла помилка: {}".format(п.повідомлення), стиль="червоний")
